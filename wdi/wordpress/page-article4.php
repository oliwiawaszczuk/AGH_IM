        <h2>Techniki Pływackie</h2>
        <p>Rozwój technik pływackich miał kluczowe znaczenie dla tego sportu. Style takie jak żabka, kraul czy grzbietowy wykształciły się w XIX wieku i do dzisiaj stanowią podstawę rywalizacji sportowej. Styl motylkowy, który jest jedną z najtrudniejszych technik, powstał stosunkowo niedawno, bo dopiero w latach 30. XX wieku.</p>

        <h2>Pływanie na Igrzyskach Olimpijskich</h2>
        <p>Pływanie zadebiutowało na pierwszych nowożytnych igrzyskach olimpijskich w 1896 roku w Atenach. Były to wyłącznie zawody dla mężczyzn, a kobiety mogły konkurować dopiero od 1912 roku w Sztokholmie. Obecnie pływanie olimpijskie obejmuje różnorodne style i dystanse, a gwiazdy takie jak Michael Phelps czy Katie Ledecky zdobywają setki medali i biją rekordy światowe.</p>

