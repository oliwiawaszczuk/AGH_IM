        <h2>Pływanie jako Dyscyplina Sportowa</h2>
        <p>Pływanie jako sport rozwijało się w Europie już w XVIII wieku. W Anglii w 1837 roku powstały pierwsze oficjalne zawody pływackie, organizowane na otwartym basenie w Londynie. Pływanie szybko zyskało na popularności, a kolejne państwa zaczęły wprowadzać własne konkurencje i techniki.</p>

